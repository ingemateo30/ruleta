    void Modificar()
    {
        String SqlCambiarV = "";
        try
        {
            SqlCambiarV = "UPDATE PARAMETROS SET VALOR = '"+TxtValor.getText()+"' WHERE CODIGO = '"+TxtCodigo.getText()+"'";
            ConeBD.StmBD.executeUpdate(SqlCambiarV);
            JOptionPane.showMessageDialog(null, "Parametro Modificado ...");
            Limpiar();
            Apagar();
            MostrarParametros();
        }
        catch (Exception err)
        {
            err.printStackTrace();
        }
    }
    
