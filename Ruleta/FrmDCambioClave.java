    void Apagar()
    {
        TxtId.setEnabled(false);
        TxtNombre.setEnabled(false);
    }
    void Limpiar()
    {
        TxtClaveAnterior.setText("");
        TxtConNewClave.setText("");
        TxtNewClave.setText("");
    }
    
    void CambiarClave()
    {
        String SqlCabiarClave = "";
        try
        {
            if (TxtNewClave.getText().equals(TxtConNewClave.getText()))
            {
                try
                {
                   SqlCabiarClave = "UPDATE SEGURIDAD SET CLAVE = '"+TxtConNewClave.getText()+"' WHERE ID = '"+TxtId.getText()+"'";
                   ConeBD.StmBD.executeUpdate(SqlCabiarClave);
                   JOptionPane.showMessageDialog(null, "Cambio Realizado Con Exito ...");
                   Limpiar();
                   dispose();
                }
                catch (Exception err)
                {
                    err.printStackTrace();
                }
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Las Claves No Son Iguales ...");
            }
        }
        catch(Exception err)
        {
            err.printStackTrace();
        }
    }
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (TxtClaveAnterior.getText().equals(ClaveAnt))
        {
            CambiarClave();
        }
        else
        {
            JOptionPane.showMessageDialog(null, "La Clave no Coincide ...");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

